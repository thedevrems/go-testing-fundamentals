package addition

// Addition prend deux entiers a et b, et renvoie leur somme.
//
// Exemple d'utilisation :
//   result := Addition(2, 3)
//   fmt.Println(result) // Affiche 5
//
// Paramètres :
//   a (int) : Le premier entier à additionner.
//   b (int) : Le second entier à additionner.
//
// Retour :
//   int : La somme des deux entiers a et b.
func Addition(a, b int) int {
	return a + b
}
